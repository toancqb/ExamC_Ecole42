#include <unistd.h>

void	ft_maff(void)
{
	char c;
	char tmp;

	c = 'a';
	while (c <= 'z')
	{
		if ((int)c % 2 != 0)
			write(1, &c, 1);
		else
		{
		   tmp = c - 32;
		   write(1, &tmp, 1);
		}
		c++;
	}
	write(1, "\n", 1);
}

int		main(void)
{
	ft_maff();
	return (0);
}
