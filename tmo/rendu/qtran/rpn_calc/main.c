#include "ft_lib.h"


int		main(int argc, char *argv[])
{
	char	**tab;
	int		i;

	i = 0;
	if (argc != 2)
		printf("Error\n");
	else
	{
		tab = ft_split(argv[1]);
		while (tab[i] != NULL)
		{
			printf("%s - ", tab[i]);
			i++;
		}
	}
}
