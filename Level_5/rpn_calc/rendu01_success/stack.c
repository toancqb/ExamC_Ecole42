#include "ft_lib.h"

int st[1000];
int head;

void	st_init()
{
	head = -1;
}

void	push(int value)
{
	st[++head] = value;
}

int		pop()
{
	return (st[head--]);	
}

int		nb_elem()
{
	return (head + 1);
}
